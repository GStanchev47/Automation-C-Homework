using System;
using System.Collections.Generic;
using System.Text;

namespace Homework_4___Loops
{
	public class Task_2
	{
		public void SecondTask()
		{
			int number = 8;

			for (int i = 1; i <= 10 ; i++)
			{
				Console.WriteLine(number + " * " + i + " = " + number * i);				
			}

			Console.WriteLine();
			Console.WriteLine("______________________________");
			Console.WriteLine();
		}

	}
}
